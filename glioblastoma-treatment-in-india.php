                                            <li><strong>Fluorescence-guided surgery (5-ALA):</strong> The patient is given an oral dose of 5-aminolevulinic acid (5-ALA) several hours before surgery. Glioblastoma cells metabolize this compound into protoporphyrin IX, which glows bright pink-violet under blue-violet light. This allows the surgeon to visualize tumor margins in real time, distinguishing malignant tissue from normal brain with remarkable accuracy. Studies show that 5-ALA-guided surgery achieves complete resection in up to 65% of patients, compared to approximately 36% with conventional microsurgery alone.</li>
                                            <li><strong>Neuronavigation (image-guided surgery):</strong> A frameless stereotactic navigation system uses preoperative MRI data to create a three-dimensional map of the brain and tumor. During surgery, the system tracks the position of surgical instruments in real time relative to this map, providing millimeter-level accuracy. This technology is especially valuable for tumors located near eloquent brain areas or deep within the brain parenchyma.</li>
                                            <li><strong>Awake craniotomy:</strong> For glioblastomas located in or near language, motor, or sensory areas (eloquent cortex), awake craniotomy allows the patient to remain conscious during the critical phase of tumor removal. The patient performs language tasks, moves limbs, or responds to stimuli while the surgeon maps functional brain areas using direct cortical stimulation. This technique enables more aggressive resection in eloquent areas while dramatically reducing the risk of permanent neurological deficits.</li>
                                            <li><strong>Intraoperative MRI (iMRI):</strong> Real-time MRI scanning during surgery allows the surgeon to assess the extent of resection before closing, identifying any residual tumor that can be safely removed. This technology has been shown to significantly improve rates of gross total resection.</li>
                                            <li><strong>Intraoperative ultrasound:</strong> Provides real-time imaging during surgery to help guide tumor removal and identify residual disease, particularly useful when iMRI is not available.</li>
                                            <li><strong>Microsurgical technique:</strong> High-powered surgical microscopes with excellent illumination and magnification allow precise dissection along tumor-brain interfaces, preserving critical blood vessels and neural structures.</li>
                                        </ul>

                                        <h3 class="mb-0 mt-4">Radiation Therapy</h3>
                                        <p class="mb-0">Radiation therapy is a critical component of glioblastoma treatment, typically beginning 2-4 weeks after surgery to allow adequate wound healing. The standard protocol involves <strong>external beam radiation therapy</strong> delivering a total dose of 60 Gray (Gy) administered in 30 fractions over 6 weeks. This fractionated approach maximizes tumor cell kill while allowing normal brain tissue time to repair between treatments.</p>

                                        <p class="mb-0">Advanced radiation techniques used for GBM treatment in India include:</p>
                                        <ul class="mb-0">
                                            <li><strong>Intensity-Modulated Radiation Therapy (IMRT):</strong> Shapes the radiation beam to conform precisely to the tumor cavity and surrounding at-risk areas, reducing radiation exposure to adjacent healthy brain tissue. IMRT is particularly important for tumors near sensitive structures such as the optic nerves, brainstem, or hippocampi.</li>
                                            <li><strong>3D Conformal Radiation Therapy (3D-CRT):</strong> Uses CT and MRI-based planning to deliver radiation beams that match the three-dimensional shape of the tumor bed, ensuring adequate coverage while minimizing collateral damage.</li>
                                            <li><strong>Volumetric Modulated Arc Therapy (VMAT):</strong> An advanced form of IMRT that delivers radiation continuously as the machine rotates around the patient, resulting in shorter treatment times and potentially more uniform dose distribution.</li>
                                        </ul>
                                        <p class="mb-0">For elderly patients (over 70) or those with poor performance status, hypofractionated radiation protocols &mdash; delivering higher doses per fraction over a shorter course (e.g., 40 Gy in 15 fractions over 3 weeks) &mdash; have been shown to provide comparable survival benefits with reduced treatment burden.</p>

                                        <h3 class="mb-0 mt-4">Chemotherapy (Temozolomide)</h3>
                                        <p class="mb-0"><strong>Temozolomide (TMZ)</strong> is the standard chemotherapy agent for glioblastoma and the cornerstone of the Stupp protocol. It is an oral alkylating agent that crosses the blood-brain barrier effectively, making it uniquely suited for treating brain tumors. Temozolomide works by adding methyl groups to DNA, causing damage that leads to tumor cell death.</p>

                                        <p class="mb-0">The effectiveness of temozolomide is closely linked to the <strong>MGMT (O6-methylguanine-DNA methyltransferase) promoter methylation status</strong> of the tumor. MGMT is a DNA repair enzyme that can reverse the damage caused by temozolomide. Approximately 35-45% of glioblastomas have a methylated (silenced) MGMT promoter, meaning the repair enzyme is not produced, making these tumors significantly more sensitive to temozolomide. Patients with MGMT-methylated tumors typically achieve median survival of 21-23 months, compared to 12-15 months for unmethylated tumors.</p>

                                        <p class="mb-0">Temozolomide is administered in two phases:</p>
                                        <ul class="mb-0">
                                            <li><strong>Concurrent phase:</strong> 75 mg/m&#178; daily for 42 consecutive days alongside radiation therapy, with prophylactic anti-nausea medication and monitoring for lymphocyte depletion (requiring Pneumocystis pneumonia prophylaxis).</li>
                                            <li><strong>Adjuvant phase:</strong> 150-200 mg/m&#178; for 5 days of each 28-day cycle, for 6-12 cycles. The dose is escalated from 150 mg/m&#178; in the first cycle to 200 mg/m&#178; in subsequent cycles if blood counts remain adequate.</li>
                                        </ul>

                                        <h3 class="mb-0 mt-4">Targeted Therapy &amp; Immunotherapy</h3>
                                        <p class="mb-0">While the Stupp protocol remains the standard of care, several additional therapeutic approaches are available or under investigation for glioblastoma:</p>
                                        <ul class="mb-0">
                                            <li><strong>Bevacizumab (Avastin):</strong> An anti-angiogenic monoclonal antibody that blocks vascular endothelial growth factor (VEGF), inhibiting the tumor's blood supply. Bevacizumab is approved for recurrent glioblastoma and has been shown to reduce tumor-related edema, decrease steroid requirements, and improve progression-free survival. While it may not significantly extend overall survival, it can meaningfully improve quality of life and neurological function in recurrent disease.</li>
                                            <li><strong>Immune checkpoint inhibitors:</strong> Drugs such as nivolumab and pembrolizumab, which have shown remarkable success in other cancers, are being actively studied in glioblastoma clinical trials. While initial results have been modest, combination approaches and patient selection based on tumor mutational burden are being explored.</li>
                                            <li><strong>Dendritic cell vaccines:</strong> Personalized vaccines created from the patient's own immune cells, trained to recognize and attack glioblastoma-specific antigens. Several clinical trials are showing encouraging early results.</li>
                                            <li><strong>CAR-T cell therapy:</strong> Chimeric antigen receptor T-cell therapy, where the patient's T cells are engineered to target specific proteins on glioblastoma cells (such as EGFRvIII), is in clinical trials with promising preliminary data.</li>
                                            <li><strong>Lomustine (CCNU):</strong> A nitrosourea chemotherapy agent used in combination with other treatments, particularly for recurrent GBM or in patients with MGMT-methylated tumors.</li>
                                        </ul>

                                        <h3 class="mb-0 mt-4">Tumor Treating Fields (TTFields/Optune)</h3>
                                        <p class="mb-0"><strong>Tumor Treating Fields (TTFields)</strong>, marketed as the Optune device, represent a newer treatment modality for glioblastoma. TTFields deliver low-intensity, intermediate-frequency (200 kHz) alternating electric fields to the tumor through transducer arrays placed on the patient's shaved scalp. These electric fields disrupt cell division (mitosis) by interfering with the alignment of charged cellular components during cell division, leading to cell death specifically in rapidly dividing tumor cells while largely sparing normal brain cells that divide much more slowly.</p>

                                        <p class="mb-0">The landmark EF-14 clinical trial demonstrated that adding TTFields to maintenance temozolomide after chemoradiation improved median overall survival from 16 months to 20.9 months, with the 5-year survival rate improving from 5% to 13%. TTFields are most effective when worn for at least 18 hours per day. While the device requires continuous use and commitment from the patient, it has a relatively mild side effect profile, with the most common adverse effect being skin irritation at the transducer array sites.</p>

                                        <p class="mb-0">TTFields are currently approved as an adjunct to maintenance temozolomide for newly diagnosed glioblastoma and as monotherapy for recurrent glioblastoma. Availability and cost continue to evolve, and Dr. Saroha can advise patients on whether TTFields may be an appropriate addition to their treatment plan.</p>

