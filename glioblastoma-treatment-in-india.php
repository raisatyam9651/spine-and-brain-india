                                        <h2 class="mb-0" style="margin-top:30px;">The Stupp Protocol: Standard of Care for GBM</h2>
                                        <p class="mb-0">The <strong>Stupp protocol</strong>, established through a landmark 2005 clinical trial led by Dr. Roger Stupp, revolutionized glioblastoma treatment and remains the gold standard worldwide. This protocol demonstrated that combining temozolomide chemotherapy with radiation therapy significantly improved survival compared to radiation therapy alone. The original trial showed a median survival improvement from 12.1 months to 14.6 months, and the 2-year survival rate improved from 10.4% to 26.5%.</p>

                                        <p class="mb-0">The Stupp protocol consists of three sequential phases:</p>
                                        <ul class="mb-0">
                                            <li><strong>Phase 1 &mdash; Maximal safe surgical resection:</strong> The most complete tumor removal that can be achieved without causing unacceptable neurological harm. Ideally, gross total resection (removal of all visible tumor on MRI) is the goal, with studies showing that resection of greater than 98% of the enhancing tumor provides optimal survival benefit.</li>
                                            <li><strong>Phase 2 &mdash; Concurrent chemoradiation (6 weeks):</strong> Beginning 2-4 weeks after surgery, the patient receives 60 Gy of focused radiation in 30 daily fractions (Monday through Friday for 6 weeks) with simultaneous daily oral temozolomide at 75 mg/m&#178;. During this phase, patients require regular blood count monitoring and receive prophylactic trimethoprim-sulfamethoxazole to prevent Pneumocystis pneumonia.</li>
                                            <li><strong>Phase 3 &mdash; Adjuvant temozolomide (6-12 cycles):</strong> After a 4-week break following chemoradiation, patients begin adjuvant temozolomide at 150-200 mg/m&#178; for 5 consecutive days of each 28-day cycle. Treatment continues for a minimum of 6 cycles and may be extended to 12 cycles depending on tolerance and response. Blood counts are monitored before each cycle.</li>
                                        </ul>

