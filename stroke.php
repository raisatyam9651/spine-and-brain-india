                                        <h3 class="mb-0">Stroke</h3>
                                        <p class="mb-0">A stroke is a medical emergency that occurs when the blood supply to the brain is disrupted, resulting in damage to brain cells. Treatment for stroke typically involves a combination of medications and supportive care, and may also include procedures such as thrombolysis or thrombectomy to remove blood clots and restore blood flow to the brain.</p>
                                        <p class="mb-0">The first step in treating a stroke is to properly diagnose the condition and determine the type of stroke. This may involve imaging tests such as a CT scan or MRI, as well as blood tests and other diagnostic tests. Once the type of stroke has been identified, the treatment plan can be tailored to the specific needs of the patient.</p>
                                        <p class="mb-0">Medications can be effective in treating certain types of stroke and preventing further damage to the brain. For example, clot-busting medications such as tPA (tissue plasminogen activator) can be given to break up blood clots and restore blood flow to the brain. Other medications may be used to control blood pressure, prevent blood clots, and manage other stroke-related complications.</p>
                                        <p class="mb-0">In some cases, procedures such as thrombolysis or thrombectomy may be necessary to remove blood clots and restore blood flow to the brain. These procedures are typically performed by a neuro interventionalist, and may be used in combination with medications to improve the chances of a successful outcome.</p>
                                        <p class="mb-0">Overall, the goal of treatment for stroke is to minimize brain damage and prevent further complications.</p>
                                        
