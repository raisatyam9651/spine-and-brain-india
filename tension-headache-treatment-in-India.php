                                        <h2 class="mt-5 mb-3">Symptoms and Signs of Tension Headaches</h2>
                                        <p>Recognizing the symptoms of a tension headache is important for accurate self-identification and knowing when to seek professional help. Tension headaches have a distinctive symptom profile that differentiates them from other headache types such as <a href="migraine-treatment-in-India" title="Migraine treatment in India">migraines</a> and <a href="cluster-headache-treatment-in-India" title="Cluster headache treatment in India">cluster headaches</a>.</p>

                                        <h3>Primary Symptoms</h3>
                                        <ul class="mb-3">
                                            <li><strong>Bilateral dull, pressing pain:</strong> The hallmark symptom is a non-pulsating, dull ache or pressure that affects both sides of the head. Patients often describe it as a tight band or vice grip squeezing around the forehead, temples, or back of the head.</li>
                                            <li><strong>Mild to moderate intensity:</strong> Unlike migraines, the pain of a tension headache is typically mild to moderate. It does not usually prevent you from performing daily activities, though it can significantly reduce concentration and productivity.</li>
                                            <li><strong>No nausea or vomiting:</strong> Tension headaches are generally not accompanied by nausea or vomiting. If you regularly experience nausea with your headaches, it may indicate a migraine disorder and should be evaluated by a specialist.</li>
                                            <li><strong>Minimal sensitivity to light and sound:</strong> While migraines cause severe photophobia and phonophobia, tension headaches may cause only mild sensitivity to light or sound, and never both simultaneously.</li>
                                            <li><strong>Not worsened by physical activity:</strong> Routine physical activities such as walking or climbing stairs do not typically worsen tension headache pain, which is another key differentiator from migraines.</li>
                                            <li><strong>Duration of 30 minutes to 7 days:</strong> Individual episodes can last anywhere from 30 minutes to a full week. Chronic tension headaches may persist for most of the day, every day, creating a constant background of discomfort.</li>
                                        </ul>

                                        <h3>Associated Symptoms</h3>
                                        <ul class="mb-3">
                                            <li><strong>Scalp tenderness:</strong> Many patients experience tenderness of the scalp, neck, and shoulder muscles. You may notice discomfort when brushing your hair, wearing a hat, or pressing on certain spots on the head and neck.</li>
                                            <li><strong>Muscle tightness in the neck and shoulders:</strong> A sense of stiffness or tightness in the cervical and trapezius muscles is extremely common and often precedes or accompanies the headache.</li>
                                            <li><strong>Difficulty concentrating:</strong> The persistent dull pain can impair focus, memory, and cognitive performance, particularly affecting work productivity and academic performance.</li>
                                            <li><strong>Irritability and fatigue:</strong> Chronic headache pain often leads to mood disturbances, including increased irritability, frustration, and mental exhaustion.</li>
                                            <li><strong>Sleep disturbances:</strong> Tension headaches can make it difficult to fall asleep or maintain quality sleep, creating a self-perpetuating cycle of headache and poor rest.</li>
                                        </ul>

