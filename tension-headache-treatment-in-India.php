                                        <p>A thorough neurological examination assesses cranial nerve function, reflexes, muscle strength, coordination, sensation, and cognitive function. This helps rule out secondary headache disorders caused by underlying neurological conditions. Dr. Saroha also performs palpation of the pericranial muscles (scalp, temporalis, masseter, sternocleidomastoid, and trapezius muscles) to assess for tenderness, which is a key clinical finding in tension headache patients.</p>

