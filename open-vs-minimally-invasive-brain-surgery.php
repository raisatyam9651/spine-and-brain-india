                                        <!-- OPEN BRAIN SURGERY (CRANIOTOMY) -->
                                        <h2 class="mb-0" style="margin-top:30px;">Open Brain Surgery (Craniotomy)</h2>
                                        <p class="mb-0"><strong>Open brain surgery</strong>, also known as a craniotomy, involves removing a section of the skull (bone flap) to gain direct access to the brain. This traditional approach remains the gold standard for many types of brain tumors and continues to be the most widely performed neurosurgical procedure worldwide.</p>

                                        <h3 class="mb-0 mt-4">When Is Open Craniotomy Used?</h3>
                                        <p class="mb-0">Open surgery is typically recommended in the following scenarios:</p>
                                        <ul class="mb-0">
                                            <li><strong>Large tumors (greater than 4&ndash;5 cm)</strong> that require extensive access for complete removal</li>
                                            <li><strong>Surface-level tumors</strong> such as convexity meningiomas that are easily accessible through a craniotomy</li>
                                            <li><strong>Malignant tumors</strong> like glioblastoma where maximum safe resection with wide margins is critical</li>
                                            <li><strong>Highly vascular tumors</strong> where direct visualization and control of blood vessels is essential</li>
                                            <li><strong>Tumors involving multiple lobes</strong> or those that have grown into surrounding brain tissue</li>
                                            <li><strong>Recurrent tumors</strong> where previous surgery has altered the anatomy</li>
                                        </ul>

                                        <h3 class="mb-0 mt-4">Advantages of Open Craniotomy</h3>
                                        <ul class="mb-0">
                                            <li>Provides the widest possible surgical field and direct visualization of the tumor</li>
                                            <li>Allows the surgeon to identify and protect critical brain structures under direct vision</li>
                                            <li>Enables complete removal of large or complex tumors in a single operation</li>
                                            <li>Well-established technique with decades of published outcomes data</li>
                                            <li>Suitable for almost any tumor type and location when minimally invasive options are not feasible</li>
                                        </ul>

                                        <h3 class="mb-0 mt-4">Limitations of Open Surgery</h3>
                                        <ul class="mb-0">
                                            <li>Larger incision and skull opening result in more tissue disruption</li>
                                            <li>Longer hospital stay (5&ndash;7 days) and recovery period (4&ndash;6 weeks)</li>
                                            <li>Higher risk of infection (3&ndash;5%) compared to minimally invasive approaches</li>
                                            <li>Greater blood loss during the procedure</li>
                                            <li>Visible scarring, though usually concealed by hair</li>
                                            <li>More postoperative pain and discomfort</li>
                                        </ul>

                                        <!-- MINIMALLY INVASIVE APPROACHES -->
                                        <h2 class="mb-0" style="margin-top:30px;">Minimally Invasive Approaches</h2>
                                        <p class="mb-0"><strong>Minimally invasive brain surgery</strong> refers to a group of advanced surgical techniques that allow neurosurgeons to access and remove brain tumors through smaller openings. These approaches use specialized instruments, high-definition endoscopes, microscopes, and computer-guided navigation systems to achieve precise tumor removal while minimizing damage to healthy brain tissue. Dr. Arun Saroha employs several minimally invasive techniques depending on the tumor's characteristics:</p>

                                        <h3 class="mb-0 mt-4">Endoscopic Transsphenoidal Surgery (Pituitary Tumors)</h3>
                                        <p class="mb-0">This is the preferred approach for <a href="pituitary-adenoma-treatment-in-india" title="Pituitary Adenoma Treatment in India"><strong>pituitary adenomas</strong></a> and other tumors at the base of the skull. The surgeon accesses the tumor through the nasal passages &mdash; no external incision is required. A thin endoscope is inserted through the nose and sphenoid sinus to reach the pituitary gland directly. This technique offers several benefits:</p>
                                        <ul class="mb-0">
                                            <li>No external scars or visible incisions</li>
                                            <li>Minimal disruption to brain tissue since the approach avoids the brain entirely</li>
                                            <li>Shorter hospital stay of 2&ndash;3 days</li>
                                            <li>Faster return to normal activities within 1&ndash;2 weeks</li>
                                            <li>Success rate exceeding 90% for pituitary adenomas</li>
                                            <li>Lower risk of complications compared to transcranial approaches for the same tumors</li>
                                        </ul>

                                        <h3 class="mb-0 mt-4">Keyhole Craniotomy (Supraorbital and Retrosigmoid)</h3>
                                        <p class="mb-0"><strong>Keyhole brain surgery</strong> uses a small skull opening of approximately 2&ndash;3 cm to access tumors that would traditionally require a much larger craniotomy. The two primary keyhole approaches are:</p>
                                        <ul class="mb-0">
                                            <li><strong>Supraorbital (eyebrow) approach:</strong> A small incision is made within the eyebrow crease, providing access to tumors in the frontal lobe, anterior skull base, and around the optic nerves. The scar is virtually invisible once healed. This approach is suitable for anterior skull base meningiomas, craniopharyngiomas, and selected frontal lobe tumors.</li>
                                            <li><strong>Retrosigmoid (behind-the-ear) approach:</strong> A small incision is made behind the ear to access tumors in the posterior fossa, cerebellopontine angle, and brainstem region. This is commonly used for acoustic neuromas, epidermoid tumors, and certain posterior fossa meningiomas.</li>
                                        </ul>
                                        <p class="mb-0">Keyhole craniotomies offer the precision of microsurgery with the benefits of a smaller opening &mdash; less pain, reduced swelling, lower infection risk, and faster recovery compared to traditional craniotomy.</p>

                                        <h3 class="mb-0 mt-4">Endoscopic Intraventricular Surgery</h3>
                                        <p class="mb-0">Tumors located within the brain's ventricular system (the fluid-filled cavities inside the brain) can often be reached using a thin endoscope inserted through a small burr hole. This technique is particularly effective for:</p>
                                        <ul class="mb-0">
                                            <li><strong>Colloid cysts</strong> &mdash; benign cysts in the third ventricle that can cause sudden, dangerous blockage of cerebrospinal fluid</li>
                                            <li><strong>Intraventricular tumors</strong> such as choroid plexus papillomas, subependymomas, and certain ependymomas</li>
                                            <li><strong>Hydrocephalus</strong> caused by tumors obstructing CSF flow, where endoscopic third ventriculostomy (ETV) can bypass the blockage</li>
                                        </ul>
                                        <p class="mb-0">The endoscopic approach avoids the need to cut through large areas of brain tissue to reach deep ventricular tumors, significantly reducing the risk of neurological deficits.</p>

