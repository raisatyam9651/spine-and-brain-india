                                        <h3>Abortive (Acute) Medications</h3>
                                        <p>Abortive medications are taken at the onset of a migraine to stop or reduce the attack. The key to effectiveness is early treatment — ideally within the first 30 minutes of symptom onset. Options include:</p>
                                        <ul class="mb-3">
                                            <li><strong>Triptans (Sumatriptan, Rizatriptan, Zolmitriptan)</strong> — the gold standard for moderate to severe migraine attacks, these serotonin receptor agonists constrict dilated blood vessels and block pain pathways. Available as tablets, nasal sprays, and injections for fast relief</li>
                                            <li><strong>NSAIDs (Ibuprofen, Naproxen, Diclofenac)</strong> — effective for mild to moderate migraines, especially when combined with antiemetics. Best taken early in the attack</li>
                                            <li><strong>Ergotamines (Ergotamine, Dihydroergotamine)</strong> — older medications that constrict blood vessels, useful for prolonged attacks. Available as nasal spray and injection</li>
                                            <li><strong>Anti-emetics (Metoclopramide, Domperidone)</strong> — control nausea and vomiting while also improving absorption of oral pain medications</li>
                                            <li><strong>Gepants (Ubrogepant, Rimegepant)</strong> — newer CGRP receptor antagonists that can be used for both acute treatment and prevention, without the cardiovascular risks of triptans</li>
                                            <li><strong>Ditans (Lasmiditan)</strong> — a newer class that works on serotonin 1F receptors without causing blood vessel constriction, suitable for patients with cardiovascular disease</li>
                                        </ul>

                                        <h3>Preventive Medications</h3>
                                        <p>Preventive therapy is recommended when patients experience 4 or more migraine days per month, when attacks are severe and prolonged, or when abortive medications are ineffective or overused. Options include:</p>
                                        <ul class="mb-3">
                                            <li><strong>Beta-blockers (Propranolol, Metoprolol)</strong> — first-line preventive agents with strong evidence base, also beneficial for patients with coexisting hypertension or anxiety</li>
                                            <li><strong>Antidepressants (Amitriptyline, Venlafaxine)</strong> — particularly useful when migraine coexists with depression, anxiety, or insomnia. Amitriptyline is effective for both migraine and <a href="tension-headache-treatment-in-India" title="Tension headache treatment">tension headache</a> prevention</li>
                                            <li><strong>Anticonvulsants (Topiramate, Valproate)</strong> — proven to reduce migraine frequency by 50% or more. Topiramate has the added benefit of potential weight loss</li>
                                            <li><strong>CGRP Inhibitors</strong> — the newest and most targeted preventive medications (detailed below)</li>
