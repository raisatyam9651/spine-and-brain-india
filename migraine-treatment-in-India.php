                                        <h2 class="mt-5 mb-3">Diagnostic Process for Migraines</h2>
                                        <p>Accurate diagnosis is the foundation of effective migraine treatment. At Spine and Brain India, <strong>Dr. Arun Saroha</strong> employs a thorough, evidence-based diagnostic protocol to differentiate migraines from other headache types and identify any underlying conditions that may be contributing to your symptoms. This comprehensive approach ensures that you receive the correct diagnosis and the most appropriate treatment plan.</p>

                                        <h3>Headache Diary</h3>
                                        <p>Patients are encouraged to maintain a detailed headache diary for at least 4-8 weeks before their consultation. This diary should record the date, time, and duration of each attack; pain location, intensity, and quality; associated symptoms (nausea, aura, sensitivity); potential triggers (food, sleep, stress, weather, menstrual cycle); medications taken and their effectiveness; and the impact on daily activities. This information is invaluable for identifying patterns, confirming the diagnosis, and tailoring a personalized treatment strategy.</p>

                                        <h3>Neurological Examination</h3>
                                        <p>A comprehensive neurological examination assesses cranial nerve function, motor strength, sensory perception, reflexes, coordination, balance, and cognitive function. This examination helps rule out secondary causes of headache such as <a href="brain-tumor-surgery-in-India" title="Brain tumor surgery in India">brain tumors</a>, <a href="brain-aneurysm-treatment-in-India" title="Brain aneurysm treatment in India">brain aneurysms</a>, meningitis, or other structural abnormalities.</p>

                                        <h3>Imaging and Laboratory Tests</h3>
                                        <p><strong>MRI (Magnetic Resonance Imaging)</strong> of the brain is the gold standard imaging study for headache evaluation, providing detailed visualization of brain structures to rule out tumors, vascular malformations, white matter lesions, and other structural causes. <strong>CT (Computed Tomography)</strong> scans are used for urgent evaluation, particularly to rule out hemorrhage. <strong>Blood tests</strong> may be ordered to check for thyroid dysfunction, inflammatory markers, hormonal imbalances, vitamin deficiencies (particularly B12, D, and magnesium), and complete blood count abnormalities.</p>

                                        <h3>ICHD-3 Diagnostic Criteria</h3>
                                        <p>Dr. Saroha uses the International Classification of Headache Disorders, 3rd Edition (ICHD-3) criteria for formal migraine diagnosis. For migraine without aura, this requires at least 5 attacks lasting 4-72 hours with at least two of four pain characteristics (unilateral, pulsating, moderate-severe intensity, aggravated by physical activity) and at least one associated symptom (nausea/vomiting or photophobia plus phonophobia). Adherence to these standardized criteria ensures diagnostic accuracy and consistency.</p>

