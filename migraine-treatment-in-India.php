                                        <h2 class="mt-5 mb-3">Migraine Symptoms and the Four Phases of a Migraine Attack</h2>
                                        <p>A migraine attack is not simply a headache — it is a multi-phase neurological event that can last anywhere from several hours to several days. Understanding the four distinct phases of a migraine can help patients recognize early warning signs, take timely action, and communicate more effectively with their specialist. Not every patient experiences all four phases, and the phases can vary in duration and intensity from one attack to another.</p>

                                        <h3>Phase 1: Prodrome (Warning Phase) — 1 to 2 Days Before</h3>
                                        <p>The prodrome phase occurs in approximately 60% of migraine sufferers, appearing hours to days before the headache. Subtle warning signs include mood changes (irritability, depression, euphoria), food cravings (especially for sweet or salty foods), increased yawning, neck stiffness, constipation, increased thirst and urination, and difficulty concentrating. Recognizing these early signals can allow patients to take abortive medication before the headache phase fully develops, significantly improving treatment effectiveness.</p>

                                        <h3>Phase 2: Aura Phase — 5 to 60 Minutes</h3>
                                        <p>The aura phase affects 20-30% of migraine patients and typically lasts 5 to 60 minutes. Aura symptoms are fully reversible and develop gradually over at least 5 minutes. Visual auras are the most common and include scintillating scotoma (an expanding arc of shimmering, zigzag light), blind spots, flashing lights, or distorted vision. Sensory auras include tingling or numbness that spreads up the arm to the face. Speech and language auras involve difficulty finding words or slurred speech. Rarely, motor auras cause temporary weakness on one side of the body (hemiplegic migraine).</p>

                                        <h3>Phase 3: Headache Phase — 4 to 72 Hours</h3>
                                        <p>The headache phase is the most debilitating, lasting <strong>4 to 72 hours</strong> without treatment. Key characteristics include:</p>
                                        <ul class="mb-3">
                                            <li><strong>Throbbing, pulsating pain</strong> — typically on one side of the head (unilateral), though it can be bilateral</li>
                                            <li><strong>Moderate to severe intensity</strong> — pain that interferes with or prevents normal daily activities</li>
                                            <li><strong>Nausea and vomiting</strong> — present in up to 80% of migraine attacks</li>
                                            <li><strong>Photophobia</strong> — extreme sensitivity to light, even normal room lighting</li>
                                            <li><strong>Phonophobia</strong> — extreme sensitivity to sound, including normal conversation</li>
                                            <li><strong>Osmophobia</strong> — sensitivity to smells that may trigger nausea</li>
                                            <li><strong>Worsening with physical activity</strong> — even walking or climbing stairs intensifies the pain</li>
                                            <li><strong>Allodynia</strong> — skin sensitivity where normal touch becomes painful (combing hair, wearing glasses)</li>
                                        </ul>

                                        <h3>Phase 4: Postdrome (Recovery Phase) — 24 to 48 Hours</h3>
                                        <p>After the headache resolves, most patients enter the postdrome phase, often described as a "migraine hangover." Symptoms include fatigue and exhaustion, difficulty concentrating, mood changes (either euphoria or depression), muscle weakness, reduced appetite, and continued sensitivity to light and sound at lower levels. Many patients report that sudden head movements during the postdrome can briefly trigger a flash of pain in the location of the resolved headache.</p>

