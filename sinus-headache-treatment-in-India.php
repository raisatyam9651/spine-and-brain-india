                                            <li><strong>Post-nasal drip</strong> — mucus draining down the back of the throat, causing throat irritation, cough, and sometimes nausea</li>
                                            <li><strong>Bad breath (halitosis)</strong> — infected sinus discharge draining into the throat produces an unpleasant odor</li>
