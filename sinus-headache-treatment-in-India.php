                                        <!-- Section 5: Causes of Sinus Headaches -->
                                        <h2 class="mt-5 mb-3">Causes of Sinus Headaches</h2>
                                        <p>Sinus headaches develop when the sinuses become inflamed, blocked, or infected. The underlying causes are varied and often interrelated:</p>

                                        <h3 class="mt-3 mb-2">Infectious Causes</h3>
                                        <ul class="mb-3">
                                            <li><strong>Acute viral sinusitis</strong> — the most common cause, typically following a cold or upper respiratory infection. Viral sinusitis accounts for the majority of acute sinusitis cases and usually resolves within 7-10 days</li>
                                            <li><strong>Acute bacterial sinusitis</strong> — develops when a viral infection creates conditions favorable for bacterial growth, or as a secondary infection. Bacterial sinusitis is suspected when symptoms worsen after initial improvement or persist beyond 10 days</li>
                                            <li><strong>Fungal sinusitis</strong> — caused by fungal organisms, particularly in immunocompromised patients or in hot, humid climates common in many parts of India. Allergic fungal sinusitis is an important variant that causes thick, tenacious mucus and nasal polyps</li>
                                            <li><strong>Dental infections</strong> — infections in the upper teeth, particularly the molars and premolars, can spread to the maxillary sinuses due to their close anatomical proximity, causing odontogenic sinusitis</li>
                                        </ul>

                                        <h3 class="mt-3 mb-2">Allergic and Inflammatory Causes</h3>
                                        <ul class="mb-3">
                                            <li><strong>Allergic rhinitis</strong> — allergies to dust mites, pollen, mold, pet dander, and other airborne allergens cause chronic nasal and sinus inflammation. India's high pollution levels and tropical climate make allergic rhinitis particularly prevalent</li>
                                            <li><strong>Non-allergic rhinitis</strong> — chronic nasal inflammation triggered by irritants, temperature changes, hormonal fluctuations, or medications rather than allergens</li>
                                            <li><strong>Nasal polyps</strong> — benign growths in the nasal passages or sinuses that block mucus drainage and create chronic sinus pressure</li>
                                        </ul>

                                        <h3 class="mt-3 mb-2">Structural Causes</h3>
                                        <ul class="mb-3">
                                            <li><strong>Deviated nasal septum</strong> — a crooked or off-center nasal septum restricts airflow and impairs sinus drainage on one side, predisposing to recurrent sinusitis</li>
                                            <li><strong>Concha bullosa</strong> — air-filled enlargement of the middle turbinate bone that can obstruct sinus drainage</li>
                                            <li><strong>Narrow sinus ostia</strong> — naturally small or narrow sinus drainage openings that are easily blocked by even mild swelling</li>
                                        </ul>

                                        <h3 class="mt-3 mb-2">Environmental and Lifestyle Causes</h3>
                                        <ul class="mb-3">
                                            <li><strong>Air pollution</strong> — India's urban areas have among the highest pollution levels globally, causing chronic irritation and inflammation of the nasal and sinus linings</li>
                                            <li><strong>Swimming</strong> — chlorinated pool water and pressure changes during diving can irritate sinus linings and force water into the sinuses</li>
                                            <li><strong>Smoking and secondhand smoke</strong> — cigarette smoke paralyzes the cilia (tiny hair-like structures) that sweep mucus out of the sinuses, leading to mucus stagnation and infection</li>
                                            <li><strong>Immune deficiency</strong> — weakened immune function due to diabetes, HIV, chemotherapy, or other conditions increases susceptibility to sinus infections</li>
                                            <li><strong>Dry air and air conditioning</strong> — prolonged exposure to dry, air-conditioned environments dries out nasal mucosa, impairing its protective function</li>
                                        </ul>

