                                        <h2 class="mb-0" style="margin-top:30px;">Causes &amp; Risk Factors for Brain Tumors in India</h2>
                                        <p class="mb-0">While the exact cause of most brain tumors remains unknown, several established <strong>brain tumor risk factors</strong> can increase the likelihood of developing these growths:</p>
                                        <ul class="mb-0">
                                            <li><strong>Genetic Mutations &amp; Predisposition:</strong> Inherited or spontaneous DNA changes in brain cells can trigger abnormal cell growth. Conditions such as neurofibromatosis type 1 and 2, Li-Fraumeni syndrome, Von Hippel-Lindau disease, and tuberous sclerosis significantly increase brain tumor risk. A <strong>genetic predisposition</strong> accounts for approximately 5-10% of all brain tumor cases.</li>
                                            <li><strong>Radiation Exposure:</strong> Prior exposure to ionizing radiation, particularly from radiation therapy for previous cancers (especially childhood cancers), is a well-documented risk factor. The risk increases with higher radiation doses and younger age at exposure.</li>
                                            <li><strong>Family History:</strong> Having a first-degree relative (parent, sibling, or child) with a brain tumor slightly increases risk, though most brain tumors are not hereditary.</li>
                                            <li><strong>Age-Related Risks:</strong> Certain tumors are more common at specific ages. Medulloblastoma and pilocytic astrocytoma are more common in children, while glioblastoma and meningioma are predominantly adult tumors, with incidence increasing after age 50.</li>
                                            <li><strong>Gender Variations:</strong> Meningiomas occur more frequently in women (possibly linked to hormonal factors), while glioblastomas are slightly more common in men. Overall, males have a marginally higher incidence of brain tumors than females.</li>
                                            <li><strong>Occupational Exposures:</strong> Workers exposed to certain industrial chemicals, pesticides, solvents, and formaldehyde may have increased risk, though evidence remains inconclusive. Rubber manufacturing and oil refining workers show slightly elevated rates.</li>
                                            <li><strong>Environmental Factors:</strong> Exposure to certain chemicals, toxins, and viral infections may contribute to increased risk. Research continues to investigate the role of electromagnetic fields and mobile phone radiation, though current evidence does not establish a definitive link.</li>
                                            <li><strong>Immune System Conditions:</strong> Individuals with weakened immune systems, such as those with HIV/AIDS or those taking immunosuppressive medications after organ transplants, have a higher risk of developing brain lymphomas.</li>
                                        </ul>

