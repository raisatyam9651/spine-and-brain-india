                                        <h3 class="mb-0 mt-4">Types of Brain Tumors</h3>
                                        <p class="mb-0">Brain tumors are broadly classified into primary tumors (originating in the brain) and secondary or metastatic tumors (spreading from other body parts):</p>

                                        <h4 style="margin-top:15px;margin-bottom:5px;">Primary Brain Tumors</h4>
                                        <ul class="mb-0">
                                            <li><strong>Glioblastoma (GBM):</strong> The most aggressive malignant brain tumor, glioblastoma accounts for about 15% of all brain tumors. It grows rapidly and requires aggressive multimodal treatment including surgery, radiation, and chemotherapy. Dr. Saroha has extensive experience in maximizing safe resection of glioblastoma while preserving neurological function.</li>
                                            <li><strong>Astrocytoma:</strong> A type of glioma arising from star-shaped brain cells called astrocytes. Astrocytomas range from low-grade (slow-growing) to high-grade (aggressive). Treatment approach varies based on the tumor grade and location.</li>
                                            <li><strong>Meningioma:</strong> The most common benign brain tumor, meningioma develops from the meninges (membranes surrounding the brain). Most meningiomas are slow-growing and have excellent surgical outcomes, with <strong>90-95% success rates</strong> for complete removal.</li>
                                            <li><strong>Pituitary Adenoma:</strong> Tumors of the pituitary gland that can affect hormone production. These are typically benign and are often removed through minimally invasive <strong>endoscopic transsphenoidal surgery</strong>, offering faster recovery and minimal scarring.</li>
                                            <li><strong>Acoustic Neuroma (Vestibular Schwannoma):</strong> A benign tumor on the nerve connecting the ear to the brain. Microsurgical removal achieves success rates exceeding 95%.</li>
                                            <li><strong>Oligodendroglioma:</strong> Slow-growing tumors that respond well to a combination of surgery and chemotherapy, with favorable long-term outcomes.</li>
                                            <li><strong>Medulloblastoma:</strong> The most common malignant brain tumor in children, typically found in the cerebellum. Treatment involves surgery followed by radiation and chemotherapy.</li>
                                        </ul>

                                        <h4 style="margin-top:15px;margin-bottom:5px;">Secondary (Metastatic) Brain Tumors</h4>
                                        <p class="mb-0">Secondary brain tumors spread to the brain from cancers originating in other organs. The most common sources include:</p>
                                        <ul class="mb-0">
                                            <li><strong>Lung cancer</strong> &mdash; the most frequent source of brain metastases</li>
                                            <li><strong>Breast cancer</strong> &mdash; commonly spreads to the brain in advanced stages</li>
                                            <li><strong>Colon cancer</strong> &mdash; can metastasize to the brain through the bloodstream</li>
                                            <li><strong>Kidney cancer</strong> and <strong>melanoma</strong> &mdash; also known to cause brain metastases</li>
                                        </ul>
                                        <p class="mb-0">Metastatic brain tumors are more common than primary tumors and require a coordinated treatment approach involving neurosurgery, oncology, and radiation therapy.</p>

