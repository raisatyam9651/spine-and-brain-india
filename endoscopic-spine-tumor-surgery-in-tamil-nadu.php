                                        <h3 class="mb-0"> What is Spine Tumors?</h3>
                              
                                        <p class="mb-0">A spine tumor is an abnormal growth of cells that develops within or around the spinal cord or vertebrae. These tumors can be benign (non-cancerous) or malignant (cancerous) and may originate in the spine (primary tumors) or spread from other parts of the body (metastatic tumors). Spinal tumors can cause pain, neurological symptoms, and even affect mobility, depending on their location and size.</p>
                                        
                                        
                                        <h3 class="mt-3">Symptoms of Spinal Tumors</h3>
                                        <p>Reduced sensation or muscle weakness in the chest, arms, and particularly legs.</p>
                                         <ul class="mb-0 list-unstyled">
                                            <li>Difficulty walking, potentially leading to falls.</li>
                                            <li>Stiffness in the neck or back.</li>
                                            <li>Pain and neurological symptoms (like tingling) that worsen with movement.</li>
                                            <li>Decreased sensitivity to cold, heat, and pain.</li>
                                            <li>Loss of bladder or bowel control.</li>
                                            <li>Paralysis in various parts of the body, depending on the affected nerves.</li>
                                            <li>Development of scoliosis or other spinal deformations.</li>
                                           
                                        </ul>
                                        
                                        <h3 class="mb-0 mt-3">Types of Spinal Tumors</h3>
                                        <p>Spinal tumors can be classified based on their location within the spine or the origin of the cells. Here are the main types of spinal tumors:</p>
                                        
                                        <ul class="mb-0 mt-3 list-unstyled">
                                            <li><b>Intradural-Extramedullary Tumors: </b>These tumors develop within the dura mater (the outer layer of the spinal cord) but outside the spinal cord itself. Common examples include:
                                            <ul class="mb-0 mt-3 list-unstyled">
                                                <li><b>Meningiomas: </b>Tumors that form in the membranes surrounding the spinal cord, typically benign.</li>
                                                <li><b>Schwannomas and Neurofibromas: </b>These arise from the nerve roots that come off the spinal cord, usually benign.</li>
                                            </ul></li>
                                            
                                            
                                            <li><b>Intramedullary Tumors:</b> These tumors grow inside the spinal cord or within the nerves themselves. They are often found within the cervical (neck) region. Types include:
                                            <ul class="mb-0 mt-3 list-unstyled">
                                                <li><b>Astrocytomas:</b> Typically slow-growing tumors that originate from the cells called astrocytes.</li>
                                                <li><b>Ependymomas:</b> Originating from ependymal cells that line the central canal of the spinal cord.</li>
                                            </ul></li>
                                            
                                            <li><b>Extradural Tumors:</b> These are the most common type of spinal tumors and are located outside the dura mater. They may invade the spinal column, bones, or discs. Often, these are metastatic cancers that have spread from other parts of the body. Types include:
                                            <ul class="mb-0 mt-3 list-unstyled">
                                                <li><b>Metastatic tumors:</b> Secondary tumors that have spread from cancer in another part of the body, such as breast, lung, or prostate cancer.</li>
                                                
                                                <li><b>Primary bone cancers:</b> Such as osteosarcoma or chondrosarcoma, which originate in the spinal bones.</li>
                                            </ul></li>
                                            
                                            
                                        </ul>
                                        
                                        
                                        
                                        
