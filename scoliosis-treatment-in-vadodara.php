                                        <h3 class="mb-0">Understanding Scoliosis</h3>
                                        <p class="mb-0">Scoliosis is a condition where the spine curves sideways, often in an "S" or "C" shape. It can affect people of all ages, but it is most commonly diagnosed in children and teenagers during growth spurts. In many cases, the exact cause of scoliosis is unknown, though it can sometimes be related to conditions like muscular dystrophy or cerebral palsy. While some individuals may have mild scoliosis that doesn't require treatment, others may experience significant curvature that can lead to back pain, difficulty breathing, and other health problems.</p>
                                        
                                      
                                        <h3 class="mt-3">What is Scoliosis Treatment?</h3>
                                        <p class="mb-0">Scoliosis treatment depends on the severity of the spine's curvature and the age of the patient. For mild cases, doctors may recommend regular monitoring to see if the curve worsens. If scoliosis is detected early, non-surgical treatments such as wearing a back brace can help prevent further curvature, especially in children and teenagers.</p>
                                        <p class="mb-0">In more severe cases, surgery might be necessary. The most common surgical option is spinal fusion, where the curved part of the spine is straightened using metal rods, screws, and bone grafts. This surgery helps to prevent the curve from worsening and can improve overall function and quality of life.</p>
                                      
