                                        <p class="mb-0">A head injury, also known as a traumatic brain injury, is an injury to the brain caused by a blow or jolt to the head. These injuries can range from mild to severe, and can have a wide range of short-term and long-term effects. Treatment for a head injury depends on the severity of the injury and the specific symptoms the person is experiencing.</p>
                                        <p class="mb-0">Mild head injuries, also known as concussions, are typically treated with rest and observation. The person may be advised to avoid strenuous activities and to avoid activities that could cause another head injury, such as contact sports. In most cases, symptoms of a concussion will resolve on their own within a few days to a week.</p>
                                         <p class="mb-0">Moderate to severe head injuries may require more extensive treatment. This may include medications to control swelling or seizures, and medical procedures to remove blood clots or repair damaged blood vessels. In some cases, surgery may be necessary to remove foreign objects or repair skull fractures.</p>
