                                        <!-- TYPES OF HEAD INJURIES -->
                                        <h2 class="mb-0" style="margin-top:25px;">Types of Head Injuries We Treat</h2>
                                        <p class="mb-0">Dr. Arun Saroha is experienced in diagnosing and treating the full spectrum of head injuries:</p>

                                        <ul class="mb-0">
                                            <li><strong>Concussion:</strong> A mild TBI causing temporary brain function disruption. Most patients recover fully with proper rest and monitoring.</li>
                                            <li><strong>Brain Contusion:</strong> A bruise on the brain tissue, often caused by a direct impact. May require surgical intervention if the contusion is large or causes significant swelling.</li>
                                            <li><strong>Epidural Hematoma (EDH):</strong> Bleeding between the skull and the outer brain membrane. This is a neurosurgical emergency requiring immediate evacuation of the blood clot.</li>
                                            <li><strong>Subdural Hematoma (SDH):</strong> Bleeding between the brain surface and its covering (dura mater). Can be acute (emergency) or chronic (developing over weeks).</li>
                                            <li><strong>Subarachnoid Hemorrhage:</strong> Bleeding in the space between the brain and the thin tissues covering it. Causes severe headache and requires urgent treatment.</li>
                                            <li><strong>Diffuse Axonal Injury (DAI):</strong> Widespread damage to the brain's white matter caused by rotational acceleration. One of the most devastating forms of TBI.</li>
                                            <li><strong>Skull Fractures:</strong> Linear, depressed, or basilar fractures of the skull bones. Depressed fractures often require surgical elevation.</li>
                                            <li><strong>Penetrating Brain Injuries:</strong> Foreign objects entering the brain through the skull. Require emergency surgical exploration and repair.</li>
                                        </ul>

                                        <!-- TREATMENT OPTIONS -->
                                        <h2 class="mb-0" style="margin-top:25px;">Head Injury Treatment Options</h2>
                                        <p class="mb-0">Treatment depends on the type and severity of the head injury. Dr. Saroha employs a comprehensive approach:</p>

                                        <h3 class="mb-0" style="margin-top:20px;">Non-Surgical Treatment (Mild to Moderate Injuries)</h3>
                                        <ul class="mb-0">
                                            <li><strong>Close Observation & Monitoring:</strong> Continuous neurological assessment in a dedicated ICU setting with round-the-clock monitoring</li>
                                            <li><strong>Medications:</strong> Anti-seizure drugs, osmotic diuretics (Mannitol) to reduce brain swelling, pain management, and medications to prevent secondary brain damage</li>
                                            <li><strong>CT Scan & MRI Monitoring:</strong> Serial imaging to track brain swelling, bleeding progression, or new complications</li>
                                            <li><strong>ICP Monitoring:</strong> Intracranial pressure monitoring to detect dangerous pressure buildup inside the skull</li>
                                        </ul>

                                        <h3 class="mb-0" style="margin-top:20px;">Surgical Treatment (Moderate to Severe Injuries)</h3>
                                        <ul class="mb-0">
                                            <li><strong>Craniotomy:</strong> Surgical opening of the skull to remove blood clots (hematomas), repair damaged blood vessels, or remove bone fragments</li>
                                            <li><strong>Decompressive Craniectomy:</strong> Removal of a portion of the skull to allow the swollen brain to expand, reducing life-threatening pressure. The bone flap is preserved and replaced later.</li>
                                            <li><strong>Hematoma Evacuation:</strong> Emergency removal of epidural or subdural blood clots that are compressing the brain</li>
                                            <li><strong>Depressed Fracture Elevation:</strong> Surgical lifting and repair of depressed skull fractures that are pressing on the brain</li>
                                            <li><strong>CSF Drainage:</strong> Placement of external ventricular drain (EVD) to remove excess cerebrospinal fluid and reduce intracranial pressure</li>
                                            <li><strong>Cranioplasty:</strong> Reconstruction of the skull using the patient's own bone or synthetic materials after decompressive surgery</li>
                                        </ul>

