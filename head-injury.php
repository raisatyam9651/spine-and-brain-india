                                        <!-- WHAT IS HEAD INJURY SECTION -->
                                        <h2 class="mb-0" style="margin-top:25px;">What is a Head Injury?</h2>
                                        <p class="mb-0">A head injury &mdash; medically termed a <strong>traumatic brain injury (TBI)</strong> &mdash; is any injury to the brain, skull, or scalp caused by an external mechanical force. This can include a direct blow to the head, a violent jolt or shaking, or a penetrating wound from a sharp object or projectile.</p>

                                        <p class="mb-0">Head injuries are broadly classified into two types:</p>
                                        <ul class="mb-0">
                                            <li><strong>Closed Head Injuries:</strong> The skull remains intact, but the brain is damaged by the force of impact. This includes concussions, contusions (brain bruises), and diffuse axonal injuries where nerve fibers are torn.</li>
                                            <li><strong>Open (Penetrating) Head Injuries:</strong> The skull is fractured or pierced, exposing the brain to potential infection and direct tissue damage. These often result from accidents, gunshot wounds, or falling objects.</li>
                                        </ul>

                                        <h3 class="mb-0" style="margin-top:20px;">Common Causes of Head Injuries in India</h3>
                                        <ul class="mb-0">
                                            <li><strong>Road Traffic Accidents (RTAs):</strong> The leading cause, accounting for nearly 60% of all TBI cases in India</li>
                                            <li><strong>Falls:</strong> Especially common in elderly patients and young children</li>
                                            <li><strong>Sports Injuries:</strong> Cricket, football, boxing, and cycling-related head trauma</li>
                                            <li><strong>Workplace Accidents:</strong> Construction site falls and industrial injuries</li>
                                            <li><strong>Assault and Violence:</strong> Physical attacks resulting in head trauma</li>
                                            <li><strong>Blast Injuries:</strong> Rare but severe cases from explosions</li>
                                        </ul>

                                        <h3 class="mb-0" style="margin-top:20px;">Symptoms of Head Injury &mdash; When to Seek Emergency Care</h3>
                                        <p class="mb-0">Recognizing head injury symptoms early is critical. Seek immediate medical attention if you notice any of the following after a head trauma:</p>

                                        <h4 style="margin-top:15px;margin-bottom:5px;">Mild Head Injury Symptoms:</h4>
                                        <ul class="mb-0">
                                            <li>Headache or pressure in the head</li>
                                            <li>Brief loss of consciousness (seconds to minutes)</li>
                                            <li>Dizziness, nausea, or vomiting</li>
                                            <li>Difficulty concentrating or remembering</li>
                                            <li>Ringing in the ears (tinnitus)</li>
                                            <li>Sensitivity to light or noise</li>
                                        </ul>

                                        <h4 style="margin-top:15px;margin-bottom:5px;">Severe Head Injury Symptoms (Call Emergency Immediately):</h4>
                                        <ul class="mb-0">
                                            <li>Prolonged loss of consciousness</li>
                                            <li>Repeated vomiting or persistent nausea</li>
                                            <li>Seizures or convulsions</li>
                                            <li>Unequal pupil size (one pupil larger than the other)</li>
                                            <li>Clear fluid leaking from the nose or ears (CSF leak)</li>
                                            <li>Weakness or numbness in arms or legs</li>
                                            <li>Slurred speech or inability to speak</li>
                                            <li>Increasing confusion, agitation, or unusual behavior</li>
                                            <li>Worsening headache that does not go away</li>
                                        </ul>

