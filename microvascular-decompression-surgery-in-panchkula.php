                                        <h2 class="mb-0"> What is Microvascular Decompression?</h2>
                              
                                        <p class="mb-0">Microvascular decompression (MVD) is a surgical treatment is used to address conditions where a blood vessel is pressing on a cranial nerve, causing pain or spasms.  </p>
                                        
                                        
                                        <h3 class="mb-0 mt-3">The most common conditions treated with MVD are:</h3>
                                        
                                         <ul class="mb-0 list-unstyled">
                                            <li><b>Trigeminal Neuralgia: </b>Sudden, intense, shock-like facial pain on one side of the face.</li>
                                            <li><b>Hemifacial Spasm: </b> Uncontrollable twitching on one side of the face.</li>
                                            <li><b>Glossopharyngeal Neuralgia:</b> Rare condition causing intense pain in the throat, ears, back of the tongue, or tonsils.</li>
                                            
                                        </ul>
                                
                                        
                                        
                                        <h3 class="mb-0 mt-3">How is Microvascular Decompression Surgery done ?</h3>
                                        <p class="mb-0">Microvascular decompression (MVD) surgery involves performing a craniotomy, which opens the skull to access and relieve nerves at the brainstem's base. This procedure is often used to treat trigeminal neuralgia. During MVD, a sponge is placed between the nerve and any compressing vessels, effectively shielding the nerve from pulsating vessel pressure and alleviating the intense pain caused by nerve compression. This separation is what gives microvascular decompression its name and its efficacy in treating painful neural conditions.</p>
                                        
                                        
