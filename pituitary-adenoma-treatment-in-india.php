                                        <h2 class="mb-0" style="margin-top:30px;">What is a Pituitary Adenoma?</h2>
                                        <p class="mb-0">A <strong>pituitary adenoma</strong> is a benign (non-cancerous) tumor that develops in the pituitary gland &mdash; a small, pea-sized organ located at the base of the brain behind the bridge of the nose. Often called the "master gland," the pituitary controls the production of essential hormones that regulate growth, metabolism, reproduction, blood pressure, and stress response. When a tumor grows in this gland, it can disrupt the delicate hormonal balance of the entire body and, if large enough, compress surrounding structures such as the optic nerves.</p>

                                        <p class="mb-0">Pituitary adenomas are remarkably common. Autopsy and radiological studies suggest that up to <strong>1 in 5 people</strong> may harbour a small pituitary adenoma without ever knowing it. Clinically significant pituitary tumors that require treatment account for approximately <strong>10&ndash;15% of all diagnosed intracranial tumors</strong>, making them one of the most frequently encountered brain-region tumors in neurosurgical practice. The good news is that the vast majority of these tumors are benign, slow-growing, and carry an excellent prognosis when treated by an experienced neurosurgeon.</p>

                                        <p class="mb-0"><strong>Pituitary adenoma treatment in India</strong> has advanced significantly over the past decade. With state-of-the-art endoscopic technology available at centres like Max Super Speciality Hospital and expert neurosurgeons like <strong>Dr. Arun Saroha</strong>, patients now have access to world-class, minimally invasive surgical options that deliver outcomes comparable to the best international centres &mdash; at a fraction of the cost.</p>

                                        <!-- TYPES OF PITUITARY ADENOMAS -->
                                        <h2 class="mb-0" style="margin-top:30px;">Types of Pituitary Adenomas</h2>
                                        <p class="mb-0">Pituitary adenomas are classified along two important axes: by their hormonal activity and by their size. Understanding the type of adenoma is critical for determining the most appropriate treatment strategy.</p>

                                        <h3 class="mb-0 mt-4">Functioning vs Non-Functioning Adenomas</h3>
                                        <p class="mb-0"><strong>Functioning (secretory) adenomas</strong> produce excess hormones, leading to specific clinical syndromes depending on which hormone is overproduced. They account for roughly 65&ndash;70% of all pituitary adenomas and are usually detected earlier because of their hormonal effects. <strong>Non-functioning (non-secretory) adenomas</strong> do not produce active hormones in excess. They are typically discovered when they grow large enough to cause mass-effect symptoms such as headaches or vision loss, or incidentally during brain imaging for unrelated reasons.</p>

                                        <h3 class="mb-0 mt-4">Classification by Size</h3>
                                        <ul class="mb-0">
                                            <li><strong>Microadenomas (&lt;10 mm):</strong> Small tumors confined within the sella turcica (the bony cavity that houses the pituitary). They generally have the best surgical outcomes and lowest recurrence rates.</li>
                                            <li><strong>Macroadenomas (&gt;10 mm):</strong> Larger tumors that may extend beyond the sella and compress the optic chiasm, cavernous sinuses, or brain tissue. These require more complex surgical planning but still carry good success rates with experienced surgeons.</li>
                                            <li><strong>Giant adenomas (&gt;40 mm):</strong> Rare, very large tumors that pose significant surgical challenges and may require staged operations or multimodal treatment.</li>
                                        </ul>

                                        <h3 class="mb-0 mt-4">Common Functioning Adenoma Subtypes</h3>
                                        <ul class="mb-0">
                                            <li><strong>Prolactinoma (Prolactin-Secreting):</strong> The most common functioning pituitary tumor, accounting for approximately 40% of all pituitary adenomas. Excess prolactin causes galactorrhea (abnormal milk production), menstrual irregularities in women, erectile dysfunction in men, and infertility. Prolactinomas often respond well to medical therapy with dopamine agonists such as Cabergoline.</li>
                                            <li><strong>Growth Hormone (GH)-Secreting Adenoma:</strong> Excess growth hormone causes <strong>acromegaly</strong> in adults (enlarged hands, feet, and facial features, joint pain, diabetes) and gigantism in children. Surgical removal is the primary treatment to normalize GH levels and prevent long-term cardiovascular and metabolic complications.</li>
                                            <li><strong>ACTH-Secreting Adenoma (Cushing&rsquo;s Disease):</strong> Excess adrenocorticotropic hormone stimulates the adrenal glands to overproduce cortisol, leading to <strong>Cushing&rsquo;s syndrome</strong> &mdash; characterised by weight gain (particularly central obesity), moon face, buffalo hump, easy bruising, hypertension, diabetes, and osteoporosis. Early surgical intervention is essential as prolonged cortisol excess causes serious systemic damage.</li>
                                            <li><strong>TSH-Secreting Adenoma:</strong> The rarest functional subtype, producing excess thyroid-stimulating hormone that leads to secondary hyperthyroidism with symptoms including weight loss, rapid heartbeat, tremor, and heat intolerance. Surgical resection is the treatment of choice.</li>
                                        </ul>

                                        <!-- SYMPTOMS -->
                                        <h2 class="mb-0" style="margin-top:30px;">Pituitary Adenoma Symptoms</h2>
                                        <p class="mb-0">The symptoms of a pituitary adenoma depend on whether the tumor produces excess hormones and on its size. Patients may experience symptoms from <strong>hormonal overproduction</strong>, from <strong>compression of surrounding structures</strong>, or from <strong>pituitary hormone deficiency</strong> if the normal gland is compressed by the tumor.</p>

                                        <h3 class="mb-0 mt-4">Mass-Effect Symptoms (Due to Tumor Size)</h3>
                                        <ul class="mb-0">
                                            <li><strong>Headaches:</strong> Often dull, persistent, and located behind the eyes or in the forehead region. Headaches result from stretching of the dura mater (brain covering) or increased pressure within the sella.</li>
                                            <li><strong>Vision loss (Bitemporal Hemianopia):</strong> A hallmark finding in which patients lose peripheral (side) vision in both eyes. This occurs when the tumor grows upward and compresses the optic chiasm &mdash; the crossing point of the optic nerves. Left untreated, this can progress to significant visual impairment or blindness.</li>
                                            <li><strong>Double vision or drooping eyelid:</strong> Occurs when the tumor extends sideways into the cavernous sinus, affecting the nerves that control eye movement.</li>
                                        </ul>

                                        <h3 class="mb-0 mt-4">Hormonal Symptoms</h3>
                                        <ul class="mb-0">
                                            <li><strong>Galactorrhea:</strong> Abnormal milk discharge from the breasts (in prolactinomas), occurring in both women and, rarely, men.</li>
                                            <li><strong>Menstrual irregularity or amenorrhea:</strong> Missed or irregular periods in women, often one of the earliest symptoms of a prolactinoma.</li>
                                            <li><strong>Infertility:</strong> Disrupted reproductive hormones can prevent ovulation in women and reduce sperm production in men.</li>
                                            <li><strong>Acromegaly features:</strong> Enlargement of hands, feet, and jaw; coarsening of facial features; joint pain; excessive sweating (in GH-secreting tumors).</li>
                                            <li><strong>Cushing&rsquo;s features:</strong> Central obesity, moon-shaped face, purple stretch marks, thin skin, easy bruising, muscle weakness, and elevated blood sugar (in ACTH-secreting tumors).</li>
                                            <li><strong>Fatigue and weakness:</strong> May result from cortisol excess, thyroid dysfunction, or hypopituitarism (reduced pituitary function from tumor compression).</li>
                                            <li><strong>Decreased libido and erectile dysfunction:</strong> Commonly associated with prolactinomas and hypopituitarism in men.</li>
                                        </ul>

