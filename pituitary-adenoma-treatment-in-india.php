                                        <p class="mb-0">Microscopic transsphenoidal surgery uses an operating microscope rather than an endoscope to visualise the pituitary gland through a nasal or sublabial (under the upper lip) approach. While this technique has been the traditional standard for decades and remains effective, the endoscopic approach has largely superseded it due to superior visualisation, better tumour clearance at the margins, and improved patient comfort. Microscopic surgery may still be preferred in certain anatomical situations or based on the surgeon&rsquo;s expertise.</p>

