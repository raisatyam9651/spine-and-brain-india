                                        <p class="mb-0">For <strong>large meningiomas (over 3cm)</strong> or those causing significant symptoms, open surgery is typically the first-line treatment with a 90&ndash;95% success rate for complete removal. For <strong>small meningiomas (under 3cm)</strong>, particularly those in surgically challenging locations such as the cavernous sinus or skull base, CyberKnife or Gamma Knife radiosurgery offers excellent tumor control rates of 93&ndash;97% with minimal risk.</p>

                                        <h3 class="mb-0 mt-4">Glioblastoma (GBM)</h3>
                                        <p class="mb-0"><strong>Surgery is always the primary treatment</strong> for glioblastoma, aiming for maximum safe resection to reduce tumor burden before radiation and chemotherapy. Radiosurgery (CyberKnife or Gamma Knife) plays a valuable role in treating <strong>recurrent glioblastoma</strong> when repeat surgery is not feasible, or for targeting specific areas of tumor regrowth identified on follow-up MRI scans.</p>

                                        <h3 class="mb-0 mt-4">Pituitary Adenoma</h3>
                                        <p class="mb-0"><strong>Endoscopic transsphenoidal surgery</strong> is the preferred first-line treatment for most pituitary adenomas, offering high cure rates with minimal invasiveness through the nasal passages. <strong>Gamma Knife radiosurgery</strong> is particularly effective for residual or recurrent pituitary tumors after surgery, and for hormone-secreting adenomas that are not fully controlled by medication or initial surgery.</p>

                                        <h3 class="mb-0 mt-4">Acoustic Neuroma (Vestibular Schwannoma)</h3>
                                        <p class="mb-0">Treatment choice depends primarily on tumor size. For <strong>small acoustic neuromas (under 2.5&ndash;3cm)</strong>, Gamma Knife radiosurgery offers hearing preservation rates of 60&ndash;70% and tumor control rates exceeding 95%. For <strong>larger acoustic neuromas</strong>, microsurgical removal is recommended to prevent brainstem compression. Patient age and hearing status also influence the decision.</p>

                                        <h3 class="mb-0 mt-4">Brain Metastases</h3>
                                        <p class="mb-0">For <strong>small or multiple brain metastases</strong>, CyberKnife or Gamma Knife radiosurgery is often the preferred approach, as it can treat multiple tumors in a single session without the risks of multiple craniotomies. For a <strong>large single metastasis (over 3cm)</strong> causing significant mass effect, surgical removal followed by radiosurgery to the surgical bed may provide the best outcome. The choice also depends on the primary cancer type, extent of systemic disease, and the patient's overall prognosis.</p>

