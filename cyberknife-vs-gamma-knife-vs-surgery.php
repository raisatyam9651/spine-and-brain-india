                                        <!-- OPEN BRAIN SURGERY -->
                                        <h2 class="mb-0" style="margin-top:30px;">Open Brain Surgery (Craniotomy) &ndash; When Is It Best?</h2>
                                        <p class="mb-0">A <strong>craniotomy</strong> remains the gold standard for brain tumor treatment when direct access to the tumor is needed. During this procedure, the neurosurgeon removes a section of the skull to access and physically remove the tumor. Modern craniotomies are performed with advanced technologies including <strong>neuronavigation</strong>, <strong>intraoperative MRI</strong>, and <strong>cortical mapping</strong> to maximise safe tumor removal while preserving critical brain functions.</p>

                                        <h3 class="mb-0 mt-4">Ideal Candidates for Open Surgery</h3>
                                        <ul class="mb-0">
                                            <li><strong>Large tumors (over 3&ndash;5cm)</strong> that are too big for radiosurgery</li>
                                            <li><strong>Tumors causing mass effect</strong> &ndash; significant brain compression requiring immediate decompression</li>
                                            <li><strong>Accessible tumor locations</strong> on or near the brain surface</li>
                                            <li><strong>Tissue diagnosis needed</strong> &ndash; when biopsy is essential for determining tumor type and grade</li>
                                            <li><strong>Tumors with significant surrounding swelling</strong> (oedema) that needs relief</li>
                                            <li><strong>Cystic tumors</strong> that require drainage alongside tumor removal</li>
                                        </ul>

                                        <h3 class="mb-0 mt-4">Advantages of Open Surgery</h3>
                                        <p class="mb-0">The primary advantage of craniotomy is <strong>immediate tumor removal</strong>. Unlike radiosurgery, which works gradually over weeks to months, surgery provides instant relief from tumor-related symptoms such as headaches, seizures, and neurological deficits caused by mass effect. Surgery also provides definitive tissue diagnosis, which is crucial for planning further treatment such as chemotherapy or radiation therapy. Dr. Arun Saroha at Max Hospital uses the latest neuronavigation technology to achieve maximum safe resection with minimal disruption to healthy brain tissue.</p>

                                        <h3 class="mb-0 mt-4">Limitations</h3>
                                        <p class="mb-0">Open surgery carries inherent risks including infection, bleeding, and potential neurological deficits. Recovery requires 4&ndash;8 weeks, and the hospital stay is typically 5&ndash;10 days. Deep-seated tumors in critical brain areas may not be safely accessible through conventional surgery, making radiosurgery a better alternative in such cases.</p>

                                        <!-- CYBERKNIFE SECTION -->
                                        <h2 class="mb-0" style="margin-top:30px;">CyberKnife Radiosurgery &ndash; When Is It Best?</h2>
                                        <p class="mb-0"><strong>CyberKnife</strong> is a robotic radiosurgery system that delivers highly focused radiation beams to tumors with sub-millimetre accuracy. Unlike traditional radiation therapy, CyberKnife concentrates high doses of radiation precisely on the tumor while minimising exposure to surrounding healthy brain tissue. The system uses real-time image guidance and robotic tracking to adjust for even the slightest patient movement during treatment.</p>

                                        <h3 class="mb-0 mt-4">Ideal Candidates for CyberKnife</h3>
                                        <ul class="mb-0">
                                            <li><strong>Small to medium tumors (up to 5cm)</strong> in any location within the brain</li>
                                            <li><strong>Irregularly shaped tumors</strong> that benefit from CyberKnife's ability to conform radiation beams precisely</li>
                                            <li><strong>Tumors near critical structures</strong> such as the brainstem, optic nerves, or eloquent cortex</li>
                                            <li><strong>Patients unfit for general anesthesia</strong> due to age or medical conditions</li>
                                            <li><strong>Recurrent tumors</strong> after prior surgery or radiation</li>
                                            <li><strong>Multiple brain metastases</strong> that can be treated in a single or few sessions</li>
                                            <li><strong>Patients who prefer a non-invasive approach</strong> with minimal recovery time</li>
                                        </ul>

                                        <h3 class="mb-0 mt-4">Advantages of CyberKnife</h3>
                                        <p class="mb-0">CyberKnife's most significant advantage is its <strong>frameless design</strong>, which eliminates the need for a rigid head frame bolted to the skull (as required by Gamma Knife). This makes treatment more comfortable and allows for <strong>fractionated delivery</strong> over multiple sessions, which can be gentler on surrounding brain tissue. The robotic arm can deliver radiation from virtually any angle, making it exceptionally versatile for treating tumors in difficult locations. Treatment is entirely outpatient, and most patients return to normal activities within 1&ndash;2 days.</p>

                                        <h3 class="mb-0 mt-4">Limitations</h3>
                                        <p class="mb-0">CyberKnife does not physically remove the tumor. Instead, it damages tumor DNA to stop growth and cause gradual shrinkage over weeks to months. This means it does not provide immediate symptom relief from mass effect. Regular follow-up MRI scans are needed to monitor tumor response. Additionally, <strong>CyberKnife treatment in India</strong> typically costs &#8377;3L&ndash;&#8377;6L, which is slightly higher than open surgery.</p>

                                        <!-- GAMMA KNIFE SECTION -->
                                        <h2 class="mb-0" style="margin-top:30px;">Gamma Knife Treatment &ndash; When Is It Best?</h2>
                                        <p class="mb-0">The <strong>Gamma Knife</strong> is a specialised radiosurgery device that uses approximately 200 individual cobalt-60 sources to deliver converging gamma radiation beams to a precisely defined target. Despite its name, the Gamma Knife involves no knife or incision whatsoever. It has the longest track record of any stereotactic radiosurgery system, with over 50 years of clinical data supporting its effectiveness for specific brain conditions.</p>

                                        <h3 class="mb-0 mt-4">Ideal Candidates for Gamma Knife</h3>
                                        <ul class="mb-0">
                                            <li><strong>Small, well-defined brain tumors (under 3cm)</strong> with clear borders</li>
                                            <li><strong>Acoustic neuromas (vestibular schwannomas)</strong> &ndash; Gamma Knife has exceptional long-term data for these tumors</li>
                                            <li><strong>Pituitary adenomas</strong> &ndash; residual or recurrent tumors after surgery</li>
                                            <li><strong>Small meningiomas</strong> in locations where surgery would carry significant risk</li>
                                            <li><strong>Single or few brain metastases</strong> under 3cm</li>
                                            <li><strong>Arteriovenous malformations (AVMs)</strong> &ndash; Gamma Knife is a well-established treatment option</li>
                                            <li><strong>Trigeminal neuralgia</strong> that has not responded to medication</li>
                                        </ul>

                                        <h3 class="mb-0 mt-4">Advantages of Gamma Knife</h3>
                                        <p class="mb-0">Gamma Knife delivers extremely precise radiation with an accuracy of approximately <strong>0.15mm</strong>, making it one of the most accurate radiation delivery systems available. Its single-session treatment approach is convenient for patients, and the extensive clinical evidence base provides confidence in long-term outcomes. For small, well-defined brain lesions, Gamma Knife achieves <strong>tumor control rates of 90&ndash;97%</strong>. The treatment is completed in a few hours, and patients typically go home the same day.</p>

                                        <h3 class="mb-0 mt-4">Limitations</h3>
                                        <p class="mb-0">The main limitation of Gamma Knife is the requirement for a <strong>rigid stereotactic head frame</strong>, which is attached to the patient's skull with four pins under local anesthesia. While not severely painful, this can cause discomfort and anxiety. Gamma Knife is also <strong>limited to intracranial targets</strong> (brain only) and is most effective for small, spherical tumors under 3cm. Irregularly shaped or larger tumors are better suited to CyberKnife or surgery. <strong>Gamma Knife treatment in India</strong> costs &#8377;3.5L&ndash;&#8377;7L, making it the most expensive of the three options.</p>

