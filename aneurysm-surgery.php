                                        <p class="mb-0">An aneurysm is a bulge or swelling in a blood vessel. It occurs when the wall of the blood vessel weakens and bulges out. Aneurysms can happen in any blood vessel in the body, but they most commonly occur in the arteries in the brain and the aorta.</p>
                                        <p class="mb-0">If an aneurysm is left untreated, it can rupture, leading to serious complications and even death. Surgery is one treatment option for aneurysms. The type of surgery used to treat an aneurysm will depend on the size, location, and other factors.</p>
                                         <p class="mb-0">One surgical option for an aneurysm is called an open repair. In this type of surgery, the surgeon makes an incision in the skin and opens up the blood vessel to access the aneurysm. The aneurysm is then repaired using a synthetic patch or a piece of healthy blood vessel from elsewhere in the body.</p>
                                          <p class="mb-0">Another surgical option is called an endovascular repair. In this procedure, the surgeon uses a catheter to access the aneurysm from inside a blood vess el. The catheter is guided to the aneurysm and a device, such as a stent or coil, is used to block off the aneurysm and prevent it from rupturing.</p>
