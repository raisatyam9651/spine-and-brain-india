                                        <h3 class="mb-0">Arteriovenous Fistula</h3>
                                        <p class="mb-0">An arteriovenous fistula (AV fistula) is a surgically-created connection between an artery and a vein. This connection allows blood to bypass the capillaries, which are the smallest blood vessels in the body, and flow directly from the artery to the vein. AV fistulas are typically created to improve blood flow in patients who require hemodialysis, a treatment for kidney failure.</p>
                                        <p class="mb-0">AV fistulas are created using a surgical procedure in which a vein is surgically connected to an artery, typically in the arm. The fistula is then allowed to mature for several weeks before it can be used for hemodialysis. After the fistula is mature, a needle is inserted into the fistula to access the bloodstream for hemodialysis.</p>
                                        <p class="mb-0">Complications of AV fistulas can include bleeding, infection, or thrombosis (blood clot formation). Proper care of the fistula, including regular monitoring and cleaning, can help prevent complications.</p>

