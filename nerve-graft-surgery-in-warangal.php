                                        <h3 class="mb-0 mt-3">How is the treatment performed?</h3>
                                        <p class="mb-0">Nerve transfer surgeries are conducted in the operating room under general anesthesia, using neurosurgical methods. The procedure begins with the surgeon making an incision in the skin to expose both the healthy donor nerve and the damaged nerve. Electrical devices are then employed to assess the functionality and condition of these nerves. The healthy nerve is transferred and connected to the injured nerve. This allows the nerves to grow along the pathway created by the previously injured nerve in the spinal cord. After the nerves are successfully joined, the surgeon closes the incisions.</p>
                                        

