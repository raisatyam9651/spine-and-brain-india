                                        <!-- Section 4: Causes of Headaches -->
                                        <h2 class="mt-5 mb-3">Causes of Headaches: A Comprehensive Guide</h2>
                                        <p>Understanding what causes your headaches is the first step toward effective treatment. Dr. Arun Saroha evaluates each patient thoroughly to identify the specific triggers and underlying mechanisms behind their headache condition.</p>

                                        <h3 class="mt-4">Primary Headache Causes</h3>
                                        <ul class="mb-3">
                                            <li><strong>Muscle tension and stress:</strong> Sustained contraction of head, neck, and shoulder muscles due to physical or emotional stress</li>
                                            <li><strong>Vascular changes:</strong> Abnormal blood vessel dilation and constriction in the brain (primary mechanism in migraines)</li>
                                            <li><strong>Nerve pathway activation:</strong> Overactivation of the trigeminal nerve pathway, releasing inflammatory chemicals around brain blood vessels</li>
                                            <li><strong>Genetic predisposition:</strong> Migraines have a strong genetic component — 70-80% of migraine sufferers have a family history</li>
                                            <li><strong>Hormonal fluctuations:</strong> Estrogen level changes during menstruation, pregnancy, or menopause can trigger migraines in women</li>
                                            <li><strong>Sleep disturbances:</strong> Both insufficient sleep and oversleeping can trigger headaches</li>
                                            <li><strong>Dietary triggers:</strong> Aged cheese, alcohol (especially red wine), processed meats, MSG, artificial sweeteners, and caffeine withdrawal</li>
                                            <li><strong>Environmental factors:</strong> Bright lights, loud sounds, strong smells, weather changes, and high altitude</li>
                                            <li><strong>Posture and ergonomics:</strong> Poor posture during prolonged computer work or phone use strains neck and shoulder muscles</li>
                                            <li><strong>Dehydration:</strong> Not drinking enough water causes the brain to temporarily shrink, pulling away from the skull and triggering pain</li>
                                        </ul>

                                        <h3 class="mt-4">Secondary Headache Causes</h3>
                                        <ul class="mb-3">
                                            <li><strong>Sinus infections:</strong> Inflammation of the paranasal sinuses causes facial pain and pressure</li>
                                            <li><strong>Brain tumors:</strong> Tumors create increased pressure inside the skull, causing progressive headaches</li>
                                            <li><strong>Brain aneurysms:</strong> Weakened blood vessel walls can cause sudden, severe headaches when they expand or rupture</li>
                                            <li><strong>Meningitis:</strong> Inflammation of brain membranes causes severe headache with fever and neck stiffness</li>
                                            <li><strong>Head injuries and concussion:</strong> Trauma can cause acute and post-traumatic chronic headaches</li>
                                            <li><strong>Cervical spine disorders:</strong> Cervicogenic headaches originate from the neck region due to disc disease or arthritis</li>
                                            <li><strong>Hypertension:</strong> Severely elevated blood pressure can cause headaches, typically at the back of the head</li>
                                            <li><strong>Medication side effects:</strong> Certain medications, including blood pressure drugs, nitrates, and hormonal contraceptives</li>
                                            <li><strong>Eye conditions:</strong> Glaucoma, refractive errors, and eye strain can cause headaches</li>
                                            <li><strong>TMJ disorders:</strong> Temporomandibular joint dysfunction can cause headaches radiating from the jaw to the temples</li>
                                        </ul>

