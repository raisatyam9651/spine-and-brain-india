                                        <p class="mb-0">Optic nerve tumors are rare and can be benign or malignant. They are growths that develop on the optic nerve, which is the nerve that carries visual information from the eye to the brain. These tumors can cause a variety of symptoms, including vision loss, eye pain, and changes in pupil size.</p>
                                        <p class="mb-0">Treatment for optic nerve tumors depends on the type and severity of the tumor. For benign tumors, doctors may recommend monitoring the tumor to see if it is growing. If the tumor is causing symptoms, it may be surgically removed. In some cases, radiation therapy may also be used to shrink the tumor.</p>
                                        <p class="mb-0">For malignant tumors, the main treatment is usually surgery to remove as much of the tumor as possible. This may be followed by radiation therapy or chemotherapy to kill any remaining cancer cells. In some cases, a combination of these treatments may be used.</p>
