                                <li><strong>Spinal Fusion:</strong>Spinal fusion surgery involves joining two or more vertebrae to prevent movement between them. This procedure is used to stabilize the spine and is often recommended for conditions like degenerative disc disease or severe spinal instability.</li>
                                <li><strong>Artificial Disc Replacement:</strong>In this procedure, a damaged disc is replaced with an artificial disc to maintain spinal movement and alleviate pain. It is an alternative to spinal fusion and is typically performed on younger patients with disc-related issues.</li>
                                <li><strong>Minimally Invasive Spine Surgery:</strong>Minimally invasive spine surgery (MISS) involves smaller incisions and less tissue damage than traditional open surgery, resulting in quicker recovery times and less post-operative pain. Techniques such as endoscopic surgery and microdiscectomy fall under this category and are commonly used for herniated discs and spinal stenosis.</li>
