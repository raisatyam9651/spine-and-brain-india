                                        <h3 class="mb-0">Brain Tumor</h3>
                                        <p class="mb-0">A brain tumor is a growth of abnormal cells in the tissues of the brain. There are two main types of brain tumors: malignant (cancerous) and benign (non-cancerous). Malignant brain tumors are more aggressive and can spread to other parts of the body, while benign tumors do not typically spread and can often be removed through surgery.</p>
                                        <p class="mb-0">Symptoms of a brain tumor can include headaches, seizures, changes in vision or hearing, difficulty with balance or coordination, and changes in mood or behavior. These symptoms can vary depending on the size and location of the tumor.</p>
                                         <p class="mb-0">Diagnosis of a brain tumor typically involves a combination of imaging tests, such as a CT scan or MRI, and a biopsy to confirm the type of tumor. Treatment options may include surgery to remove the tumor, radiation therapy to kill cancerous cells, and chemotherapy to destroy remaining cancer cells.</p>
                                          <p class="mb-0">It is important for individuals with a brain tumor to receive treatment from a team of experienced medical professionals, such as neurosurgeons, neurologists, and oncologists. With proper treatment, many people with brain tumors are able to lead full and active lives.</p>

