                                        <p class="mb-0">Alzheimer's disease is a neurodegenerative disorder that causes a progressive decline in cognitive function, including memory, thinking, and reasoning abilities. It is the most common cause of dementia in older adults and is characterized by the buildup of abnormal protein structures in the brain. This buildup, known as amyloid plaques and neurofibrillary tangles, can lead to the death of brain cells and the loss of cognitive function. Symptoms of Alzheimer's disease typically develop slowly and worsen over time, eventually leading to a loss of the ability to carry out daily activities. There is currently no cure for Alzheimer's disease, but treatments are available to help manage the symptoms and improve quality of life.</p>

