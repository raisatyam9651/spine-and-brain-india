                                        <h3 class="mb-0">Scoliosis</h3>
                                        <p class="mb-0">Scoliosis is a condition that causes the spine to curve to the side. It is most commonly diagnosed during adolescence, but it can also affect adults. The severity of the condition can vary, and treatment options will depend on the specific curvature of the spine and the age of the individual. In mild cases, no treatment may be necessary, but in more severe cases, treatment can include the use of a back brace or surgery.</p>
                                        <p class="mb-0">For adolescents with mild scoliosis, a back brace may be recommended to prevent the curvature from worsening. The brace is worn under the clothing and works by applying gentle pressure to the spine to help straighten the curvature. It is important for the brace to be worn as prescribed by the doctor in order to be effective.</p>
                                        <p class="mb-0">In more severe cases of scoliosis, surgery may be necessary to correct the curvature of the spine. The most common type of surgery for scoliosis is spinal fusion, which involves permanently joining two or more vertebrae together to straighten the spine. This surgery can be performed using traditional open surgery or with the use of minimally invasive techniques.</p>
                                        <p class="mb-0">After surgery, physical therapy may be recommended to help the individual regain strength and mobility. It is important for individuals with scoliosis to continue with regular check-ups with their doctor to monitor the condition and ensure that it is not worsening. With proper treatment, individuals with scoliosis can lead active and healthy lives.</p>
                                       
                                        
