                                        <h3 class="mb-0">2. Coiling</h3>
                                <p class="mb-0">Coiling, also known as endovascular embolization, is a less invasive procedure than clipping. It involves threading a thin, flexible tube (catheter) through an artery in the groin and up into the brain. Once the catheter reaches the aneurysm, the surgeon inserts a soft platinum wire through the catheter and into the aneurysm. The wire coils up inside the aneurysm, creating a blood clot that seals off the aneurysm from the rest of the artery.</p>
