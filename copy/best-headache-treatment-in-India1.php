                                        <h3 class="mt-3">Types of Headaches</h3>
                                         <p class="mb-0">There are several types of headaches, and they are classified based on their causes and symptoms. The most common types of headaches are:</p>
                                       
                                        <ul class="mb-0 list-unstyled">
                                            <li>Tension headaches: Tension headaches are the most common type of headache, and they are caused by tension in the muscles of the head, neck, and shoulders. They can be triggered by stress, anxiety, and poor posture.</li>
                                            <li>Migraine headaches: Migraine headaches are characterized by a throbbing pain on one side of the head. They can be triggered by certain foods, smells, and environmental factors such as bright lights and loud noises.</li>
                                            <li>Cluster headaches: Cluster headaches are rare but very painful. They are characterized by intense pain that is focused around one eye or on one side of the head.</li>
                                            <li>Sinus headaches: Sinus headaches are caused by an inflammation of the sinuses, and they are characterized by pain and pressure around the eyes, nose, and forehead.</li>
                                           
                                        </ul>
                                        
                                        
