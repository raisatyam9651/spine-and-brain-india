                                        <p class="mb-0">There are a variety of treatments available for migraine headaches, including medications, lifestyle changes, and other therapies. Medications used to treat migraines include over-the-counter pain relievers, triptans, and ergotamines. These medications can help to relieve the pain of migraines and may also help to prevent future attacks.</p>
                                        <p class="mb-0">In addition to medication, lifestyle changes can also be effective in managing migraines. This can include getting regular exercise, getting enough sleep, avoiding triggers such as certain foods or stress, and practicing relaxation techniques such as meditation or deep breathing.</p>
