                                        <h3 class="mb-0">Spina Bifida</h3>
                                        <p class="mb-0">Spina Bifida is a birth defect that affects the spine. It occurs when the neural tube, which forms the spinal cord and surrounding bones, does not develop properly in the womb. This can result in the incomplete formation of the spine and nervous system, which can lead to a range of physical and cognitive impairments.</p>
                                        <p class="mb-0">Treatment for spina bifida will depend on the severity of the condition and the specific symptoms present. In mild cases, treatment may not be necessary, but in more severe cases, surgery may be required to repair the spinal cord and prevent further damage. In some cases, medications may be prescribed to manage symptoms such as pain or muscle spasms.</p>
                                        <p class="mb-0">Physical therapy is often an important part of the treatment plan for individuals with spina bifida. This can help improve mobility, strength, and flexibility, and may also include the use of assistive devices such as braces or wheelchairs. In addition, special education services may be necessary for individuals with cognitive impairments.</p>
                                        <p class="mb-0">Overall, the goal of treatment for spina bifida is to manage symptoms and improve quality of life. With the right care and support, individuals with spina bifida can lead active and fulfilling lives.</p>
                                        
