                                <p class="mb-0"><strong style="color:#222222; font-size:800;">Idiopathic scoliosis- </strong> The causative agent cannot be determined. Idiopathic scoliosis can develop from childhood, and it can be divided into early-onset or late-onset. Idiopathic scoliosis can be classified as infantile, juvenile, or adolescent.</p>
