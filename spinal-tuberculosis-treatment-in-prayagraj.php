                    item.querySelector('.faq-answer').classList.remove('active');
                    item.querySelector('.faq-question').classList.remove('active');
                }
            });
            
            // Toggle current FAQ
            answer.classList.toggle('active');
            question.classList.toggle('active');
        }
        
