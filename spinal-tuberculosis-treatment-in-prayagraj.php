                                        <h3 class="mb-0"> What is Spinal Tuberculosis?</h3>
                              
                                        <p class="mb-0">Spinal tuberculosis, also known as Pott's disease, is a form of tuberculosis that affects the vertebrae of the spine. It is caused by the bacterium Mycobacterium tuberculosis, which primarily targets the lungs but can also severely impact the spinal area. If not treated promptly and effectively, spinal tuberculosis can lead to chronic back pain and potential spinal damage.</p>
                                        
                                        <h3 class="mt-3">Symptoms of Spinal Tuberculosis</h3>
                                        <p>The key symptoms of spinal tuberculosis include:</p>
                                         <ul class="mb-0 list-unstyled">
                                            <li>Persistent and severe back pain</li>
                                            <li>Swelling and tenderness along the spine</li>
                                            <li>Reduced mobility</li>
                                            <li>Fever and night sweats</li>
                                            <li>Unintended weight loss</li>
                                            <li>Neurological deficits due to spinal cord compression in advanced cases</li>
                                           
                                        </ul>
                                        
                                        
                                        
                                        <h3 class="mb-0 mt-3"> Spinal Tuberculosis Diagnosis Techniques:</h3>
                                        <ul class="mb-0 list-unstyled">
                                            <li>Blood test (CBC – Complete Blood Count)</li>
                                            <li>Tuberculin Skin Test (Mantoux test)</li>
                                            <li>Radiography of spine (X-rays, Gamma rays)</li>
                                            <li>Bone scanning</li>
                                            <li>Computed tomography (CT scan)</li>
                                            <li>Magnetic Resonance Imaging (MRI)</li>
                                        </ul>
                                        
                                        
                                        
                                        
                                        <h3 class="mb-0 mt-3">Symptoms of Spinal Tuberculosis</h3>
                                        <p class="mb-0">Spinal tuberculosis typically affects the lower thoracic and upper lumbar regions of the spine. The primary symptom experienced by those with this condition is back pain. Additional early indicators include fever, night sweats, weight loss, and general malaise. As the disease progresses, patients may find it increasingly difficult to stand, and may experience stiffness when walking, a rigid posture, and swelling at the affected site. Leg weakness is also commonly reported. Without appropriate treatment, spinal tuberculosis can result in leg paralysis.</p>
                                        
